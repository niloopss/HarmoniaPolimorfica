/**
 * Classe que representa um violão, um instrumento de corda.
 * Estende a classe abstrata {@link InstrumentoCorda}.
 *
 * @author Nivea Lins
 */
class Violao extends InstrumentoCorda {

    /**
     * Retorna uma representação em string do violão.
     *
     * @return A string "Violão".
     */
    @Override
    public String toString() {
        return "Violão";
    }
}
