/**
 * Classe que representa um tambor, um instrumento de percussão.
 * Estende a classe abstrata {@link InstrumentoPercussao}.
 *
 * @author Nivea Lins
 */
class Tambor extends InstrumentoPercussao {

    /**
     * Retorna uma representação em string do tambor.
     *
     * @return A string "Tambor".
     */
    @Override
    public String toString() {
        return "Tambor";
    }
}
